package Bit;

public class Shift {
	public static void main(String[] argv) throws Exception {
		int a = 7;
	    int b = 2;
	    int c = 3;

	    a |= 4;
	    c <<= 1;
	    b >>=1;
	    //a ^= c;
	    System.out.println("a = " + a);
	    System.out.println("b = " + b);
	    System.out.println("c = " + c); 
	    
	  }
}
